package src;

public class AccessProgram {
    private String name;
    private String in;
    private String out;
    private String exceptions;

    public AccessProgram() {}

    public String getName() {
        return name;
    }

    public String getInputs() {
        return in;
    }

    public String getOutputs() {
        return out;
    }

    public String getExceptions() {
        return exceptions;
    }
}
